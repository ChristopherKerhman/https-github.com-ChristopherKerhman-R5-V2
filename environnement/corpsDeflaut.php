  <h3 class="titreArticle">Le projet R-5</h3>
<p class="articleContexte">
  Le projet <strong>R-5</strong> propose un système de jeu générique. Vous allez pouvoir avec le système <strong>R-5</strong> créer n’importe quel contexte de jeu de figurine de différente taille, allant de l’escarmouche à de petite bataille entre une soixantaine de figurine par camps.
  Vous pourrez créer le contexte qui vous plaira, allant de toutes les périodes historiques dont vous rêvez, en passant par des contextes fantastique, post-apocalyptique, steam punk ou encore futuriste.
  Le système en lui-même est littéralement « à la carte » c’est à vous de créer le contexte de vos futurs parti.
  Vous pourrez employer des figurines déjà en votre possession, déclassé ou non par un éditeur de jeu.
  De nos jours, les possibilités de création d’univers avec des figurines particulières, via <strong>l’impression 3D</strong> ouvre encore plus le champs des
  possibles.<br /><br />
  <strong>R-5</strong> est donc un jeu « bac à sable » où vous pourrez tester une idée, un type d’armée, modifier pour ajuster à vos envies du moment.</p>

  <h3 class="sousTitreArticle">Les éléments pour jouer</h3>
  <p>
  Il vous faudra quelques dés, des pions « ordre » et « statuts », que vous pouvez télécharger avec le fichier PDF du jeu, des figurines et un terrain. Une aide de jeu « en ligne » existe pour vous facilitez la tache, permettant entre autre de résoudre 99 % des cas du jeu et accessible hors ligne si vous l’installer sur un smartphone.
  </p>
